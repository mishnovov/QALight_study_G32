#Напишіть програму, щоб отримати список, відсортований у порядку зростання за останнім елементом у кожному кортежі 
#з заданого списку непорожніх кортежів.

# Визначаємо список кортежів
tuple_doubles = [(13, 51), (37, 28), (74, 15), (42, 29)]

# Сортуємо список за останнім елементом у кожному кортежі
sorted_doubles = sorted(tuple_doubles, key=lambda x: x[-1])

# Виводимо результат
print(sorted_doubles)