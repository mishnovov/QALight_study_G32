#Написати програму Python для перетворення градусів Цельсія у Фаренгейт за формулою:
#T(℉) = T(℃) x 9/5 + 32

try:
    celsies = float(input("Введіть значення в градусах Цельсія: "))
    fahrenheit = celsies * 9/5 + 32
    print(f"{celsies} градусів за Цельсієм = {fahrenheit} градусів за Фаренгейтом")
except ValueError:
    print("Введіть числове значення для градусів Цельсія.")
