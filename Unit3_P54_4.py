#Напишіть програму Python, щоб перевірити, чи існує елемент у кортежі.

#створюємо кортеж
tuple_vowels = ('y', 'p', 'i', 'o', 'u')

#пропонуємо ввести значення для перевірки
value = (input("Введіть значення для перевірки: "))

# Перевіряємо, чи існує елемент у кортежі
if value in tuple_vowels:
    print(value, "існує у кортежі.")
else:
    print(value, "не існує у кортежі.")