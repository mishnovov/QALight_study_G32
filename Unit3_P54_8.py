#Напишіть програму для друку номерів із зазначеного списку після видалення з нього парних чисел

# Визначаємо список номерів
numbers = [11, 12, 31, 14, 15, 61, 17, 18, 19, 10]

# Видаляємо парні числа зі списку
numbers = [num for num in numbers if num % 2 != 0]

# Друкуємо номери після видалення парних чисел
for num in numbers:
    print(num)